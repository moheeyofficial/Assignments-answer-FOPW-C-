/*
لديك بعض أسماء المتغيرات مكتوبة في التعليق
كل ما عليك أن تقوم بكتابة 

Valid لو كان إسم المتغير صالح بجانب إسم المتغير و 
Not Valid لو كان غير صالح

إذا كان المتغير صالح قم بكتابة 
Good Practice لو كان إسم المتغير يتبع ال 
Best Practices و 
Bad Practice لو كان لا يتبع أحسن الممارسات

لديك مثال في بداية التعليق لتوضيح الفكرة
*/

//              الحل 

/*
  name        [ Valid | Good Practice ]
  NAME        [ Valid | Bad Practice ]
  1name       [ no Valid ] 
  __name      [ Valid ] => Bad Practice ?
  name@name   [ no Valid ] 
  name10name  [ Valid ] => Bad Practice ?
  name!name   [ no Valid ] 
  first_NAME  [ Valid ] => Good Practice ?
  first_name  [ Valid ] => best Practice ?
  firstName   [ Valid ] => best Practice ?
  first name  [ no Valid ]
  fn          [ Valid ] => Bad Practice ?
  public      [ no Valid ] 
  Public      [ Valid ] => Good Practice ?
*/
